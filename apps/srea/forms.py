from django.forms import *
from apps.srea.models import*


class UsuarioCreateForm(ModelForm):

    class Meta:
        model=Usuario
        fields= '__all__'

    def save(self, commit=True):
        data = {}
        form = super()
        try:
            if form.is_valid():
                form.save()
            else:
                data['error'] = form.errors
        except Exception as e:
            data['error'] = str(e)
        return data


